# memasukkan alas (A) dan tinggi (T) segitiga
A = int(input('Masukan Alas Segitiga:  '))
T = int(input('Masukan Tinggi Segitiga:  '))

# menghitung luas segitiga
LuasSegitiga = 0.5 * A * T

# menampilkan luas segitiga
print('Luas Segitiga: ', '{: .2f}'.format(LuasSegitiga))

